<script>
    var url = "{{url('/dashboard/chart')}}";
    var LP = new Array();
    var Nilai = new Array();
    $(document).ready(function(){
      $.get(url, function(response){
        response.forEach(function(data){
            LP.push(data.lokasi_pelanggaran);
            Nilai.push(data.nilai);
        });
        var ctx = document.getElementById("ChartSPK").getContext('2d');
            var myChart = new Chart(ctx, {
              type: 'bar',
              data: {
                  labels:LP,
                  datasets: [{
                      label: 'Nilai',
                      data: Nilai,
                      borderWidth: 1
                  }]
              },
              options: {
                  scales: {
                      yAxes: [{
                          ticks: {
                              beginAtZero:true
                          }
                      }]
                  }
              }
          });
      });
    });
    </script>
